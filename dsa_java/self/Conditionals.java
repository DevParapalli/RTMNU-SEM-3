package dsa_java.self;

public class Conditionals {
    public static void main(String[] args) {
        int a, b;

        a = 2;
        b = 3;

        if (a > b)
            System.out.println("A is greater than B");
        else if (a < b)
            System.out.println("A is less than B");
        else
            System.out.println("A is equal to B");
    }
}
